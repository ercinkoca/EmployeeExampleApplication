package personelCalisma.PersonelCalisma;

public class Iller {

	private int ilId;
	private String ilAdi;
	
	public Iller(int ilId, String ilAdi) {
		super();
		this.ilId = ilId;
		this.ilAdi = ilAdi;
	}
	
	public Iller() {
		
	}

	public int getIlId() {
		return ilId;
	}

	public void setIlId(int ilId) {
		this.ilId = ilId;
	}

	public String getIlAdi() {
		return ilAdi;
	}

	public void setIlAdi(String ilAdi) {
		this.ilAdi = ilAdi;
	}
	
	
	
}
